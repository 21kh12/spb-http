/* ----------------------------------------- HTTP Server ---------------------------------------- */
import express from 'express'


/* ---------------------------------------------------------------------------------------------- */
/*                                              INIT                                              */
/* ---------------------------------------------------------------------------------------------- */
/* ----------------------------------------- HTTP Server ---------------------------------------- */
const app = express()



/* ---------------------------------------------------------------------------------------------- */
/*                                             ROUTES                                             */
/* ---------------------------------------------------------------------------------------------- */

